public function send_requirement_mail(){
    $name         = $this->input->post('name');
    $email        = $this->input->post('email');
    $mobile       = $this->input->post('mobile');
    $requirement  = $this->input->post('requirement');

    $this->load->library('email');

    // --- Gmail SMTP Config ---
    $config = array(
        'protocol'  => 'smtp',
        'smtp_host' => 'ssl://smtp.gmail.com', // use ssl:// for Gmail
        'smtp_port' => 465,                    // SSL port
        'smtp_user' => 'mekaaditya16@gmail.com',
        'smtp_pass' => 'ojbpezjljponlepe',     // your App Password (no spaces)
        'mailtype'  => 'html',
        'charset'   => 'utf-8',
        'wordwrap'  => TRUE,
        'newline'   => "\r\n",
        'crlf'      => "\r\n"
    );

    $this->email->initialize($config);

    // Email setup
    $this->email->from('mekaaditya16@gmail.com', 'Website Contact'); // must be same as smtp_user for Gmail
    $this->email->to('mekaaditya16@gmail.com'); // where you want to receive
    $this->email->reply_to($email, $name); // user’s email for replies
    $this->email->subject('New Requirement from Website');

    $body = "
        <h3>New Requirement Received</h3>
        <p><strong>Name:</strong> {$name}</p>
        <p><strong>Email:</strong> {$email}</p>
        <p><strong>Mobile:</strong> {$mobile}</p>
        <p><strong>Requirement:</strong><br>{$requirement}</p>
    ";

    $this->email->message($body);

    // Try sending
    if ($this->email->send()) {
        echo json_encode(array(
            'success' => true,
            'message' => 'Email sent successfully'
        ));
    } else {
        // show real SMTP error
        echo json_encode(array(
            'success' => false,
            'message' => 'Failed to send email. '.$this->email->print_debugger(array('headers')).'',
        ));
    }
}
